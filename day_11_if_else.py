# python program to illustrate If-elif-else ladder
percent =76
if (percent>=90 and percent<=100):
	grade = 'A'
elif (percent>=75 and percent<90):
	grade ='B'
elif (percent>=60 and percent<75):
	grade = 'C'
elif (percent>=50 and percent<60):
	grade ='D'
else:
	grade ='F'
print(grade)

